print(f"Hexadecimaal F0 naar decimaal: {num6}")

# Tel de decimale getallen 123 en 456 bij elkaar op.
decimaal_sum = 123 + 456
print(f"Som van de decimale getallen 123 en 456: {decimaal_sum}")

