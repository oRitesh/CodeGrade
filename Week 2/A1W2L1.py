# Converteer het decimale getal 45 naar zijn binaire weergave.
num = bin(45)
print(f"Decimaal 45 naar binair: {num}")

# Zet het binaire getal 1010101 om in decimale vorm.
num2 = int('1010101', 2)
print(f"Binair 1010101 naar decimaal: {num2}")

# Tel de binaire getallen 10111 en 1101 bij elkaar op en druk het resultaat uit in binaire vorm.
binair_sum = bin(int('10111', 2) + int('1101', 2))
print(f"Som van de binaire getallen 10111 en 1101: {binair_sum}")

# Zet het decimale getal 255 om in zijn hexadecimale vorm.
num3 = hex(255)
print(f"Decimaal 255 naar hexadecimaal: {num3}")

# Converteer het hexadecimale getal 2A in decimale vorm.
num4 = int('2A', 16)
print(f"Hexadecimaal 2A naar decimaal: {num4}")

# Tel de hexadecimale getallen C4 en 3A bij elkaar op en druk het resultaat uit in hexadecimaal.
hex_sum = hex(int('C4', 16) + int('3A', 16))
print(f"Som van de hexadecimale getallen C4 en 3A: {hex_sum}")

# Converteer het binaire getal 1101 in decimale vorm.
